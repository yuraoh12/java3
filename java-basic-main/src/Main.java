public class Main {

    // 주석
    // 자바 프로그램 시작과 종료 지점
    public static void main(String args[]) {

        // 숫자 출력
        System.out.println(100);
        System.out.println(200);

        // 프로그램은 위에서 아래로 순차적 실행

        // 문자 출력
        System.out.println("차태진");
        System.out.println("홍길동");


        // 숫자, 문자 아무거나 10개정도 출력


    }
}
